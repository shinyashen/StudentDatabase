CREATE TABLE STUDENT (
    SNO CHAR(3),
    SNAME CHAR(10) NOT NULL,
    SGENDER CHAR(1) NOT NULL CHECK(SGENDER IN('F', 'M')),
    MNO CHAR(3),
    PRIMARY KEY(SNO),
    FOREIGN KEY(MNO) REFERENCES MAJOR(MNO) ON DELETE CASCADE
);

CREATE TABLE MAJOR (
    MNO CHAR(3),
    MNAME CHAR(5) NOT NULL UNIQUE,
    PRIMARY KEY(MNO)
);

CREATE TABLE COLLEGE (
    CNO CHAR(3),
    CNAME CHAR(20) NOT NULL UNIQUE,
    PRIMARY KEY(CNO)
);

CREATE TABLE SMC (
    SNO CHAR(3),
    CNO CHAR(3),
    TARGETMNO CHAR(3),
    PRIMARY KEY(SNO),
    FOREIGN KEY(SNO) REFERENCES STUDENT(SNO) ON DELETE CASCADE,
    FOREIGN KEY(CNO) REFERENCES COLLEGE(CNO) ON DELETE CASCADE,
    FOREIGN KEY(TARGETMNO) REFERENCES MAJOR(MNO) ON DELETE CASCADE
);

CREATE TABLE MYUSER (
    UNAME CHAR(20),
    UPWD CHAR(20),
    PRIMARY KEY(UNAME)
